package model;

public class Student {
	private int SNo;
	private String SName;
	private int SAge;
	public int getSNo() {
		return SNo;
	}
	public void setSNo(int sNo) {
		SNo = sNo;
	}
	public String getSName() {
		return SName;
	}
	public void setSName(String sName) {
		SName = sName;
	}
	public int getSAge() {
		return SAge;
	}
	public void setSAge(int sAge) {
		SAge = sAge;
	}
	
}
